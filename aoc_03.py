def descend(x_rate: int, y_rate: int) -> int:

    x, y = 0, 0
    hits, misses = 0, 0

    height = len(data)
    width = len(data[0])

    while y < height:

        x_position = x % width
        current_position = data[y][x_position]

        if current_position == "#":
            hits += 1
        else:
            misses += 1

        # Move
        x += x_rate
        y += y_rate

    return hits


